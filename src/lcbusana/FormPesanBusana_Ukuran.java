    private void alignTextJSpinnerLeft(){
        // Array atau daftar semua JSpinner yang ingin diubah
        JSpinner[] spinners = {jsLingkarDada, jsTinggiBadan, jsLingkarPinggang, jsLingkarPinggul, jsPanjangLengan, jsPanjangBaju,jsLebarBahu }; // Sesuaikan dengan nama spinner Anda

        for (JSpinner spinner : spinners) {
            JComponent editor = spinner.getEditor();
            if (editor instanceof JSpinner.DefaultEditor) {
                JSpinner.DefaultEditor defaultEditor = (JSpinner.DefaultEditor) editor;
                JTextField textField = defaultEditor.getTextField();
                textField.setHorizontalAlignment(SwingConstants.LEFT);
            }
        }
    }
    
