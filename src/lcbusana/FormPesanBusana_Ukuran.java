    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // Ambil nilai dari JSpinner
        int lingkarDada = (int) jsLingkarDada.getValue();
        int lingkarPinggang = (int) jsLingkarPinggang.getValue();
        int lingkarPinggul = (int) jsLingkarPinggul.getValue();
        int panjangLengan = (int) jsPanjangLengan.getValue();
        int panjangBaju = (int) jsPanjangBaju.getValue();
        int tinggiBadan = (int) jsTinggiBadan.getValue();
        int lebarBahu = (int) jsLebarBahu.getValue();

        // Validasi: Semua field harus diisi (> 0)
        if (lingkarDada <= 0 ||
                lingkarPinggang <= 0 ||
                lingkarPinggul <= 0 || 
                panjangLengan <= 0 || 
                panjangBaju <= 0 || 
                tinggiBadan <= 0 || 
                lebarBahu <= 0) {
            JOptionPane.showMessageDialog(this, "Semua input harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            //Simpan Data
            DataPesanBusana data= DataPesanBusana.getInstance();
            data.setLingkarDada(lingkarDada);
            data.setLingkarPinggang(lingkarPinggang);
            data.setLingkarPinggul(lingkarPinggul);
            data.setPanjangLengan(panjangLengan);
            data.setPanjangBaju(panjangBaju);
            data.setTinggiBadan(tinggiBadan);
            data.setLebarBahu(lebarBahu);
            
            // Pindah ke halaman berikutnya
            main.ubahPanel("btambahan");
        }    
    }//GEN-LAST:event_btnNextActionPerformed

